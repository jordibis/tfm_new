import matplotlib.pyplot as plt

epochs = range(2, 11)
inicio = 2
fin = len(epochs)

plt.figure(figsize=(12, 5)) 

for method, values in accuracies_test_list.items():
    plt.plot(epochs, values, marker='x', label=method)

plt.xlim(1, 11.8)

for epoch in range(inicio, fin):
    plt.axvline(x=epoch, color='gray', linestyle='--', linewidth=1)
    plt.text(epoch + 0.1,
             max(map(max, accuracies_test_list.values())) * 0.95,
             f"{epoch} classes",
             rotation=90,
             verticalalignment='top',
             fontsize=9,
             color='gray')

for method, values in accuracies_test_list.items():
    plt.annotate(f"{values[-1]:.2f}",
                 xy=(epochs[-1], values[-1]),
                 xytext=(epochs[-1] + 0.25, values[-1]),
                 fontsize=9,
                 arrowprops=dict(arrowstyle="->"))

plt.xticks(epochs)
plt.title('Test Accuracy vs Epochs (All Dataset)')
plt.xlabel('Epochs')
plt.ylabel('Accuracy')
plt.grid(True)

